              <ul style="margin-left:7%">
              <em><b>Don’t just pull the plug!</b></em>
              <ul>
              <li><em style="font-weight: bold; font-style: normal;">A quick reboot might solve the issue.</em><em style="font-weight: bold; font-style: italic;"> Shutdown</em> can be found by going to the <em style="font-weight: bold; font-style: normal;">Start Menu.</em> No success? You can try a hard reboot by pressing <em style="font-weight: bold; font-style: normal;"> CTRL-ALT-DEL</em> simultaneously twice in a row, but there is no way to save your work.</li>
              <br>
              <li><em style="font-weight: bold; font-style: normal;">You are forced to manually shut down the computer using the power switch if that doesn't work.</em> The computer will shut down if you hold down the power button for a few seconds (around 5-10). <em style="font-weight: bold; font-style: normal;">Restart the computer after waiting at least 10 seconds</em> for the hard drive to stop spinning.</li>
              <br>
              <li><em style="font-weight: bold; font-style: normal;">Start the programs you were using at the time of the crash if the machine restarts.</em> Some applications, such as <em style="font-weight: bold; font-style: italic;">Microsoft Word,</em> create timed backups of your work and could show you restored files. If the program alerts you that a <em style="f\ font-style: italic;">"recovered document"</em> has been found, save the file with an alternative name and compare it to the ones you've already saved.</li></ul></ul>
