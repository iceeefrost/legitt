              <h4><a href="">4. Error Messages - Write them down.</a></h4>

              <li style="list-style-type:none; margin-left:7%;">What tipped you off to the problem?<em style="font-weight: bold; font-style: normal;"> Sometimes there's an error code or message displayed on the screen.</em> Be sure to write it down—it may describe the problem and how serious it is. Be sure to document the exact wording of any error messages. This can make the job of the Help Desk much faster. Write down the entire message!</li>
            </div>
          </div>

          <div class="d-flex align-items-stretch mt-4 mt-lg-0" data-aos="zoom-in" data-aos-delay="200">
            <div class="icon-box iconbox-red">
              <h4><a href="">5. Think about what changed recently on your computer.</a></h4>
              <li style="list-style-type:none; margin-left:7%;"><em style="font-weight: bold; font-style: normal;">Try to pinpoint when the trouble first started.</em> Did the problem coincide with any recent changes?</li>
              <ul style="margin-left:7%">
                <ul>

              <li><em style="font-weight: bold; font-style: normal;">Did you change any settings?</em> If you did, you might want to change them back the way.</li>
              <li><em style = "font-weight: bold; font-style:normal;">Any peripherals been added or removed recently</em> (such as a printer or external Memory Card reader?)</li>
              <li><em style="font-weight: bold; font-style: normal;">Has anyone else been using your computer recently?</em></li>
              <li><em style="font-weight: bold; font-style: normal;">Have you downloaded anything off the Web?</em> File sharing and free MP3’s are an easy way to get a virus on your machine. </li></ul></ul>
            </div>
          </div>

          <div class="d-flex align-items-stretch mt-4 mt-lg-0" data-aos="zoom-in" data-aos-delay="300">
            <div class="icon-box iconbox-teal">
              <h4><a href="">6. The computer is on but……..</a></h4>
              <ul style="margin-left:7%">
                <ul>
              <li><em style="font-weight: bold; font-style: normal;">The keyboard and mouse are not responding, and the entire screen is stuck.</em> Consider tapping the <em style="font-weight: bold; font-style: normal;">Num Lock key.</em> It is situated above the <em style="font-weight: bold; font-style: normal;">number 7</em> on the right side of the keyboard. If you tap the <em style="font-weight: bold; font-style: normal;">Num Lock key,</em> pay attention to whether the <em style="font-weight: bold; font-style: normal;">Num Lock</em> light turns on and off. If not, the computer is fully shut off. The PC needs to be <em style="font-weight: bold; font-style: normal;">REBOOTED.</em> If the light does turn on and off, don't give up right away. It might spontaneously reanimate.</li>
              <br>
              <li>Using <em style="font-weight: bold; font-style: normal;">Alt and F4,</em> To close the window you're working in when a software program freezes or stops responding, try hitting <em style="font-weight: bold; font-style: normal;">ALT+F4.</em> This will close a frozen window and restart the machine. <em style="font-weight: bold; font-style: normal;">You'll need to reboot if the keyboard is also frozen.</em></li>
              <br>
              <li><em style="font-weight: bold; font-style: normal;">There is now a "Blue Screen."</em> "Blue Screen of Death," which is dreaded. The only thing that can be done is to restart the computer. If it persists, there is likely a serious issue with the computer.</li>
              <li><em style="font-weight: bold; font-style: normal;">There is no sound even when the pc is on.</em> On the taskbar, is there a speaker symbol next to the clock?<em style="font-weight: bold; font-style: normal;"> If you can find the speaker icon,</em> double clicking it will open the computer's <em style="font-weight: bold; font-style: normal;">audio controls (it's in the lower right corner of the screen).</em> Check to see if any of the volume controls have been muted or are at their lowest setting. Any checked <em style="font-weight: bold; font-style: normal;">Mute</em> boxes should be removed.</li>
              <br>
              <li><em style="font-weight: bold; font-style: normal;">The keyboard doesn't work, but the computer is on.</em> Try switching keyboards or connecting the keyboard to a different computer. If it doesn't function on the other computer, there is probably a problem with the keyboard. If it does, there is probably a problem with the computer or the connection to the computer if it does not work on the other computer.</li></ul></ul>
            </div>
          </div>

