              <h4><a href="#">8. Printer issues</a></h4>
              <ul style="margin-left:7%">
              <em><b>Printer does not have power indicator</b></em>
              <ul>
              <li><em style="font-weight: bold; font-style: normal;">Make sure the printer is turned on first.</em> A printer should have some light when it is turned on (typically green), indicating that it is powered up and turned on.</li>
              <br>
              <li><em style="font-weight: bold; font-style: normal;">If the printer does not have an indication light, make sure it is linked to a functioning power source.</em> By checking each end of the power cable for an outlet. Press the printer's power button after that.</li>
              <br>
              <li><em style="font-weight: bold; font-style: normal;">If your printer still doesn't receive a power status indicator after taking the aforementioned actions, it's probably because you're having major printer problems, so we advise getting in touch with the printer's maker.</em> For further instructions on how to fix or replace.</li></ul></ul>
              <br>
              <ul style="margin-left:7%">
              <em><b>Cables not connected properly</b></em>
              <ul>
              <li><em style="font-weight: bold; font-style: normal;">Two cables should be attached to your printer.</em> If your printer has a power indicator light, as previously noted, the power cable should have been validated as being attached. Make sure the printer is linked to the computer via a parallel or USB data cord.</li></ul></ul>
              <ul style="margin-left:7%">
              <em><b>Printer error (orange or blinking light)</b></em>
              <ul>
              <li><em style="font-weight: bold; font-style: normal;">Your printer ought to have a solid green light after it has finished its initial setup.</em> There may be a printer fault if the light indicator is flickering or turning orange frequently. This might be a sign of a major malfunction, such as a paper jam or a problem with the toner or ink cartridge.</li>
              <br>
              <li><em style="font-weight: bold; font-style: normal;">We advise consulting the printer instructions for troubleshooting procedures</em> or methods of determining what the status indicator is reporting if you see either of these because there is no standard for what a blinking light or an orange light implies.</li></ul></ul>
              <br>
               <ul style="margin-left:7%">
              <em><b>No paper or paper jam</b></em>
              <ul>
              <li><em style="font-weight: bold; font-style: normal;">Your printer won't be able to print without paper.</em> Ensure that paper is inserted into the printer's paper tray or cartridge. Next, make sure the printer isn't partially or completely jammed with paper. If there are any paper jams in the printer, you will need to manually remove them before it can start printing once more.</li></ul></ul>
              <br>
