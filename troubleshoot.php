              <li style="list-style-type: none; margin-left:7%;"><em style="font-weight: bold; font-style: normal;">Relax. Breathe in and out.</em> Many computer issues seem more serious than they actually are. It can appear that your data has been permanently lost since the computer appears to have crashed. However, there are many issues that prevent you from accessing your data or the network, which may often be resolved quickly.</li>
             
              <br>
              <ul style="margin-left:7%">
                <em style="margin-left:-3%;">Example:</em>
                <ul>
                <li> The majority of the files in your <em style="font-weight: bold; font-style: normal;">"My Documents"</em> folder on the <em style="font-weight: bold; font-style: normal;">"H" drive</em> at school are still there because they are saved on the server rather than on each student's personal computer in the labs and classrooms. Despite the fact that they were saved on your home's <em style="font-weight: bold; font-style: normal;">"C" drive,</em> if you check your computer, they might still be there.</li></ul>
              </ul>
              <br>
              <p style="font-style:italic;">That's not to say catastrophic data loss doesn’t happen – it can, but not very often.</p>
            </div>
