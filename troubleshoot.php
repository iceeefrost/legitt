              <li><em style="font-weight: bold; font-style: normal;">Your printer's status indicator light, which was stated above, should typically be flashing if you're having ink-related problems.</em> You might wish to skip to the next step if this is not happening. The following solutions might be worth a try, though, if you've just installed a new ink cartridge.</li>
              <em>*The printer has to have ink cartridges replaced.</em></ul></ul>
              <br>
              <ul style="margin-left:7%">
              <em><b>Printer self tests</b></em>
              <ul>
                <li><em style="font-weight: bold; font-style: normal;">Printing a test page is usually possible with printers.</em> You can check this page to see if the printer is indeed operational or not. Typically, holding down a number of keys will pass this test. If you're unsure whether your printer has this feature or how to use it, consult your handbook or the manufacturer's website.</li></ul></ul>

