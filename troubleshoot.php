              <h4><a href="#">2.  Make sure there really is a problem.</a></h4>
              <li style="list-style-type:none; margin-left:7%">Can't you start your computer? Check the basics.</p>
              <br>
              <ul>
                <ul>
              <li><em style="font-weight: bold; font-style: normal;">Check to see if the power cords, cables, and connectors are connected securely.</em> The issue is frequently brought on by a loose cable or connector (power, network, keyboard, mouse, speaker, display cable, etc.).</li>
              <br>
              <li><em style="font-weight: bold; font-style: normal;">Check to see if the surge protector or power strip you're using is turned on and functional.</em> Keep in mind that a surge protector's inability to function may be caused by its self-destructive design. Surge protectors carry the brunt of electrical shocks that are too strong to avoid damaging more expensive devices.</li>
              <br>
              <li>To ensure there is no power issue, <em style="font-weight: bold; font-style: normal;">try connecting something else directly into the electrical outlet.</em></li>
            </ul>
            </ul>
            </div>
          </div>

          <div class=" d-flex align-items-stretch mt-4 mt-lg-0" data-aos="zoom-in" data-aos-delay="300">
            <div class="icon-box iconbox-pink">
              <h4><a href="#">3. Can’t get on to the network or the internet?</a></h4>
              <ul style="margin-left:7%">
              <ul>
              <li><em style="font-weight: bold; font-style: normal;">See if anyone else around is experiencing the same issue.</em> If so, a service interruption perhaps affecting a larger area could occur.</li>
              <br>
              <li>Verify the <em style="font-weight: bold; font-style: normal;">network cable's connection to the wall and the PC.</em></li>
              <br>
              <li><em style="font-weight: bold; font-style: normal;">Check the location of the network cable connection on the computer's back.</em> There should be a small green light there. You are not obtaining a signal to the computer if the LED is not on or flashing. Contact the Help Desk or your ISP if restarting the computer doesn't resolve the issue.</li></ul></ul>
            </div>
          </div>

          <div class="d-flex align-items-stretch mt-4 mt-lg-0" data-aos="zoom-in" data-aos-delay="100">
